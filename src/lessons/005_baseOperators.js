/**
 * Addition
 */
console.log(1 + 1); // 2

/**
 * Subtraction
 */
console.log(2 - 1); // 1

/**
 * Multiplication
 */
console.log(2 * 3); // 6

/**
 * Division
 */
console.log(6 / 2); // 3

/**
 * Exponentiation
 */
console.log(2 ** 3); // 8

/**
 * Modulus
 */
console.log(7 % 3); // 1

/**
 * Increment Postfix
 */
let i = 2;
console.log(i++); // 2
console.log(i); // 3

/**
 * Increment Prefix
 */
let k = 2;
console.log(++k); // 3

/**
 * Decrement Postfix
 */
let j = 2;
console.log(j--); // 2
console.log(j); // 1

/**
 * Decrement Prefix
 */
let l = 2;
console.log(--l); // 1

/**
 * String Concatenation / Conversion
 */
console.log('1' + 2); // 12
console.log(1 + '2'); // 12
console.log(2 + 2 + '1'); // 41
console.log(4 - '1'); // 3
console.log('4' - 1); // 3
console.log('6' / '2'); // 3

/**
 * Assignment
 */
let x = 5;

/**
 * Modify-In-Place
 */
x += 2;
console.log(x); // 7
x -= 2;
console.log(x); // 5
x *= 2;
console.log(x); // 10
x /= 2;
console.log(x); // 5
